package com.cpkf.basis.reflex;

public class People {
	public String denger;
	private String name;
	private int age;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public String getDenger() {
		return denger;
	}

	public void setDenger(String denger) {
		this.denger = denger;
	}
}
